let segundos = parseInt(prompt("Ingrese los segundos a convertir"))

let minutos = segundos / 60

let horas = segundos / 3600

alert(`En ${segundos} segundos han pasado ${minutos} minutos`)
alert(`En ${segundos} segundos han pasado ${horas} horas`)
alert(`Asi que ${segundos} segundos son ${minutos} minutos y ${horas} horas.`)