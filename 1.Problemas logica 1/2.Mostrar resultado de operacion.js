let resta = numero1 - numero2
let multiplicacion = numero1 * numero2
let division = numero1 / numero2
let modulo = numero1 % numero2

alert(`suma = ${suma}
    resta = ${resta}
    multiplicacion = ${multiplicacion}
    division = ${division}
    modulo = ${modulo}`

)